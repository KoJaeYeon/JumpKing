        public void PowerBar(int positionY, int power)
        {
            int height = Background.height - 1;
            ConsoleColor color = ConsoleColor.DarkGray;
            jumpPosY = positionY;
            while (positionY < height)
            {

                height -= 62;
                if (power == 0)
                {
                    Console.BackgroundColor = color;
                    if (height < 0)
                    {
                        for (int i = 0; i < 11; i++)
                        {
                            Console.SetCursorPosition(200, 41 - i * 2);
                            Console.Write("        ");
                            Console.SetCursorPosition(200, 40 - i * 2);
                            Console.Write("        ");
                        }
                    }
                    else
                    {
                        for (int i = 0; i < 11; i++)
                        {
                            Console.SetCursorPosition(200, height + 41 - i * 2);
                            Console.Write("        ");
                            Console.SetCursorPosition(200, height + 40 - i * 2);
                            Console.Write("        ");
                        }
                    }
                }
                continue;
            }
            if(power == 1 || power == 52)
            {
                Console.BackgroundColor = color;
                for(int i = 0; i < 11; i++)
                {
                    if(height < 0)
                    {
                        Console.SetCursorPosition(200, 41 - i * 2);
                        Console.Write("        ");
                        Console.SetCursorPosition(200, 40 - i * 2);
                        Console.Write("        ");
                    }
                    else
                    {
                        Console.SetCursorPosition(200, height + 41 - i * 2);
                        Console.Write("        ");
                        Console.SetCursorPosition(200, height + 40 - i * 2);
                        Console.Write("        ");
                    }

                }
            }
            else if (power % 5 == 0)
            {
                switch (power / 5)
                {
                    case 1:
                        color = ConsoleColor.DarkBlue;
                        break;
                    case 2:
                        color = ConsoleColor.Blue;
                        break;
                    case 3:
                        color = ConsoleColor.Cyan;
                        break;
                    case 4:
                        color = ConsoleColor.Green;
                        break;
                    case 5:
                        color = ConsoleColor.DarkGreen;
                        break;
                    case 6:
                        color = ConsoleColor.DarkYellow;
                        break;
                    case 7:
                        color = ConsoleColor.Red;
                        break;
                    case 8:
                        color = ConsoleColor.Magenta;
                        break;
                    case 9:
                        color = ConsoleColor.DarkMagenta;
                        break;
                    case 10:
                        color = ConsoleColor.DarkRed;
                        break;
                }
                Console.BackgroundColor = color;
                if(height < 0)
                {
                    Console.SetCursorPosition(202, 42 - (power / 5) * 2);
                    Console.Write("    ");
                    Console.SetCursorPosition(202, 41 - (power / 5) * 2);
                    Console.Write("    ");
                }
                else
                {
                    Console.SetCursorPosition(202, height + 42 - (power / 5) * 2);
                    Console.Write("    ");
                    Console.SetCursorPosition(202, height + 41 - (power / 5) * 2);
                    Console.Write("    ");
                }


            }
            Console.ResetColor();
        }

        public int Collide(int positionX, int positionY, int direction_right)
        {
            int num = 0;
            if (direction_right > 2) // Down , Up
            {
                positionY += direction_right == 4 ? 2 : -3;

                for (int i = 0; i < 7; i++)
                {
                    num = check(positionX + (i - 3), positionY);
                    if(num > 0)
                        return num;
                }
            }
            else // Left or Right
            {
                for (int i = 0; i < 2; i++)
                {
                    for (int j = 0; j < 4; j++)
                    {
                        num = check(positionX - i +3 * direction_right, positionY - 2 + j);
                        if (num > 0)
                            return num;
                    }
                }
            }
            return 0;
        }

        private int check(int x, int y)
        {
            try
            {
                if (_background[x, y] == '▣')
                {
                    return 1;
                }
                else if (_background[x, y] == '↙')
                {
                    return 2;
                }
                else if (_background[x, y] == '↘')
                {
                    return 3;
                }
                return 0;
            }
            catch (Exception e)
            {
                return 1;
            }

        }



